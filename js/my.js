var current = 0;
function loadRandom() {
  current = Math.floor(Math.random() * recipes.length);
  var r = recipes[current];

  document.getElementById("recipe-title").textContent = r.title;
  document.getElementById("recipe-desc").textContent = r.desc;
  document.getElementById("recipe-image").src = r.img;

  document.getElementById("prep-time").textContent = r.prep;
  document.getElementById("cook-time").textContent = r.cook;
  document.getElementById("serving").textContent = r.serve;

  document.getElementById("ingredient-list").innerHTML =
    r.ingredients.map((item, i) => `<li><span>${i+1}</span> ${item}</li>`).join("");

  document.getElementById("instruction-list").innerHTML =
    r.instructions.map((step, i) => `<li><span>${i+1}</span> ${step}</li>`).join("");
}

document.getElementById("nextRecipe").addEventListener("click", loadRandom);
window.onload = loadRandom;


